// 💰 Module 4: Auto-Purchase API Credits
// ═══════════════════════════════════════════════

/**
 * Automatically buy API credits/GPU time if wallet has enough funds.
 * Supports Stripe (for OpenAI/cloud GPU credits).
 */
export async function buyApiCredits() {
    console.log('\n🛒 [FUNDER] Evaluating auto-purchase of API credits...');

    const balance = await checkWalletBalance();

    if (balance.usd < AUTO_SPEND_THRESHOLD_USD) {
        console.log(`   ⏸️ Balance ($${balance.usd.toFixed(2)}) below threshold ($${AUTO_SPEND_THRESHOLD_USD}). Skipping.`);
        return { purchased: false, reason: 'Insufficient balance' };
    }

    console.log(`   ✅ Balance ($${balance.usd.toFixed(2)}) exceeds threshold. Initiating purchase...`);

    // ── Option A: Buy OpenAI API credits via Stripe ──
    if (STRIPE_SECRET_KEY) {
        try {
            const purchaseAmount = Math.min(balance.usd * 0.5, 50); // Spend max 50% or $50

            // Stripe Payment Intent (skeleton — requires valid Stripe setup)
            const stripeRes = await axios.post('https://api.stripe.com/v1/payment_intents',
                new URLSearchParams({
                    amount: Math.floor(purchaseAmount * 100), // cents
                    currency: 'usd',
                    description: 'VibeLab Auto-Purchase: Cloud GPU Credits',
                    'metadata[source]': 'vibelab-auto-funder',
                    'metadata[timestamp]': new Date().toISOString()
                }).toString(),
                {
                    headers: {
                        'Authorization': `Bearer ${STRIPE_SECRET_KEY}`,
                        'Content-Type': 'application/x-www-form-urlencoded'
                    },
                    timeout: 15000
                }
            );

            const state = loadWalletState();
            state.totalSpent = (parseFloat(state.totalSpent) + purchaseAmount).toString();
            state.transactions.push({
                type: 'purchase',
                amount: purchaseAmount,
                currency: 'USD',
                description: 'Cloud GPU Credits via Stripe',
                stripeId: stripeRes.data?.id,
                timestamp: new Date().toISOString()
            });
            saveWalletState(state);

            console.log(`   🛒 Purchase initiated: $${purchaseAmount.toFixed(2)} in GPU credits`);
            return { purchased: true, amount: purchaseAmount, stripeId: stripeRes.data?.id };

        } catch (err) {
            console.error(`   ❌ Stripe purchase failed: ${err.response?.data?.error?.message || err.message}`);
            return { purchased: false, error: err.message };
        }
    }

    console.log('   ⚠️ STRIPE_SECRET_KEY not set. Auto-purchase disabled.');
    return { purchased: false, reason: 'No payment gateway configured' };
}

