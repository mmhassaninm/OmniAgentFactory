/**
 * Submit a patch as a Pull Request to GitHub.
 * Forks the repo, creates a branch, commits the fix, and opens a PR.
 */
export async function submitBountyPR(owner, repo, issueNumber, patchCode, commitMessage) {
    console.log(`\n📤 [FUNDER] Submitting PR for Issue #${issueNumber} to ${owner}/${repo}...`);

    if (!GITHUB_TOKEN) {
        console.warn('⚠️ [FUNDER] GITHUB_TOKEN not set. PR submission skipped (dry-run mode).');
        return {
            success: false,
            reason: 'GITHUB_TOKEN not configured',
            dryRun: true,
            wouldSubmit: { owner, repo, issueNumber, commitMessage }
        };
    }

    const headers = {
        'Authorization': `Bearer ${GITHUB_TOKEN}`,
        'Accept': 'application/vnd.github.v3+json',
        'User-Agent': 'VibeLab-BountyHunter/1.0'
    };

    try {
        // Step 1: Fork the repo (GitHub auto-handles if already forked)
        console.log('   🍴 Forking repository...');
        const forkRes = await axios.post(
            `https://api.github.com/repos/${owner}/${repo}/forks`,
            {},
            { headers, timeout: 30000 }
        );
        const forkOwner = forkRes.data.owner.login;
        const forkFullName = forkRes.data.full_name;
        console.log(`   ✅ Fork: ${forkFullName}`);

        // Step 2: Get the default branch's latest commit SHA
        const repoRes = await axios.get(
            `https://api.github.com/repos/${forkOwner}/${repo}`,
            { headers }
        );
        const defaultBranch = repoRes.data.default_branch;

        const refRes = await axios.get(
            `https://api.github.com/repos/${forkOwner}/${repo}/git/refs/heads/${defaultBranch}`,
            { headers }
        );
        const baseSHA = refRes.data.object.sha;

        // Step 3: Create a new branch
        const branchName = `vibelab-fix-${issueNumber}-${Date.now()}`;
        await axios.post(
            `https://api.github.com/repos/${forkOwner}/${repo}/git/refs`,
            { ref: `refs/heads/${branchName}`, sha: baseSHA },
            { headers }
        );
        console.log(`   🌿 Branch: ${branchName}`);

        // Step 4: Create the file with the patch
        const fileName = `vibelab_fix_issue_${issueNumber}.js`;
        const contentBase64 = Buffer.from(patchCode).toString('base64');

        await axios.put(
            `https://api.github.com/repos/${forkOwner}/${repo}/contents/${fileName}`,
            {
                message: commitMessage || `fix: resolve issue #${issueNumber} [VibeLab Auto-Bounty]`,
                content: contentBase64,
                branch: branchName
            },
            { headers }
        );
        console.log(`   📝 Committed: ${fileName}`);

        // Step 5: Open a Pull Request
        const prRes = await axios.post(
            `https://api.github.com/repos/${owner}/${repo}/pulls`,
            {
                title: `[VibeLab Auto-Fix] Issue #${issueNumber}`,
                head: `${forkOwner}:${branchName}`,
                base: defaultBranch,
                body: `## 🤖 Auto-Generated Fix by VibeLab Bounty Hunter\n\nThis PR addresses **Issue #${issueNumber}**.\n\n- Generated by local LLM\n- Verified via TDD Docker Sandbox (all tests passed)\n- Submitted automatically\n\n---\n*Powered by VibeLab Autonomous Agent System*`
            },
            { headers }
        );

        console.log(`   ✅ PR opened: ${prRes.data.html_url}`);
        return { success: true, prUrl: prRes.data.html_url, prNumber: prRes.data.number };

    } catch (err) {
        console.error(`   ❌ PR submission failed: ${err.response?.data?.message || err.message}`);
        return { success: false, error: err.message };
    }
}

