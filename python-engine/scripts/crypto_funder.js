let ethersLib = null;

async function loadEthers() {
    if (ethersLib) return ethersLib;
    try {
        ethersLib = await import('ethers');
        return ethersLib;
    } catch (e) {
        console.warn('⚠️ [FUNDER] ethers.js not installed. Install with: npm install ethers');
        return null;
    }
}

/**
 * Get or create a wallet state file for tracking funds.
 */
function loadWalletState() {
    try {
        if (fs.existsSync(WALLET_FILE)) {
            return JSON.parse(fs.readFileSync(WALLET_FILE, 'utf8'));
        }
    } catch (e) { /* */ }

    const defaultState = {
        address: null,
        totalEarned: '0',
        totalSpent: '0',
        transactions: [],
        lastChecked: null
    };

    const dir = path.dirname(WALLET_FILE);
    if (!fs.existsSync(dir)) fs.mkdirSync(dir, { recursive: true });
    fs.writeFileSync(WALLET_FILE, JSON.stringify(defaultState, null, 2), 'utf8');
    return defaultState;
}

function saveWalletState(state) {
    const dir = path.dirname(WALLET_FILE);
    if (!fs.existsSync(dir)) fs.mkdirSync(dir, { recursive: true });
    fs.writeFileSync(WALLET_FILE, JSON.stringify(state, null, 2), 'utf8');
}

/**
 * Get the wallet address from the private key.
 */
export async function getWalletAddress() {
    const ethers = await loadEthers();
    if (!ethers || !WALLET_PRIVATE_KEY) {
        return loadWalletState().address || 'NOT_CONFIGURED';
    }

    try {
        const wallet = new ethers.Wallet(WALLET_PRIVATE_KEY);
        const state = loadWalletState();
        state.address = wallet.address;
        saveWalletState(state);
        return wallet.address;
    } catch (e) {
        return 'INVALID_KEY';
    }
}

/**
 * Check the wallet's ETH balance.
 */
export async function checkWalletBalance() {
    console.log('\n💳 [FUNDER] Checking wallet balance...');

    const ethers = await loadEthers();
    if (!ethers || !WALLET_PRIVATE_KEY) {
        console.warn('⚠️ [FUNDER] Wallet not configured. Set VIBELAB_WALLET_KEY env var.');
        return { eth: '0', usd: 0 };
    }

    try {
        const provider = new ethers.JsonRpcProvider(ETHEREUM_RPC);
        const wallet = new ethers.Wallet(WALLET_PRIVATE_KEY, provider);
        const balanceWei = await provider.getBalance(wallet.address);
        const balanceEth = ethers.formatEther(balanceWei);

        // Rough ETH→USD conversion (fetch live price in production)
        let ethPrice = 2000; // Fallback
        try {
            const priceRes = await axios.get('https://api.coingecko.com/api/v3/simple/price?ids=ethereum&vs_currencies=usd', { timeout: 5000 });
            ethPrice = priceRes.data?.ethereum?.usd || 2000;
        } catch (e) { /* use fallback */ }

        const balanceUsd = parseFloat(balanceEth) * ethPrice;

        const state = loadWalletState();
        state.address = wallet.address;
        state.lastChecked = new Date().toISOString();
        saveWalletState(state);

        console.log(`   💰 Address: ${wallet.address}`);
        console.log(`   💰 Balance: ${parseFloat(balanceEth).toFixed(6)} ETH (~$${balanceUsd.toFixed(2)})`);

        return { eth: balanceEth, usd: balanceUsd, address: wallet.address };
    } catch (err) {
        console.error(`   ❌ Balance check failed: ${err.message}`);
        return { eth: '0', usd: 0, error: err.message };
    }
}

