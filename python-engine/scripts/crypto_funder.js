/**
 * After a bounty patch is created, run the full monetization pipeline:
 * 1. Submit PR to GitHub
 * 2. Claim the bounty on the platform
 * 3. Check wallet for incoming funds
 * 4. Auto-purchase API credits if threshold met
 */
export async function runFundingPipeline(owner, repo, issueNumber, patchCode) {
    console.log('═══════════════════════════════════════════════');
    console.log('  💰 VibeLab Self-Funding Pipeline v1.0');
    console.log('═══════════════════════════════════════════════\n');

    // Step 1: Submit PR
    const prResult = await submitBountyPR(
        owner, repo, issueNumber, patchCode,
        `fix: auto-resolve issue #${issueNumber} [VibeLab Bounty Hunter]`
    );

    if (prResult.success) {
        console.log(`\n✅ PR submitted: ${prResult.prUrl}`);

        // Step 2: Attempt bounty claim (if Gitcoin configured)
        await claimGitcoinBounty(`${owner}/${repo}#${issueNumber}`);
    }

    // Step 3: Check wallet
    const balance = await checkWalletBalance();

    // Step 4: Auto-purchase if applicable
    if (balance.usd >= AUTO_SPEND_THRESHOLD_USD) {
        await buyApiCredits();
    }

    console.log('\n═══════════════════════════════════════════════');
    console.log('  💰 Funding pipeline complete.');
    console.log('═══════════════════════════════════════════════');

    return { pr: prResult, balance };
}

